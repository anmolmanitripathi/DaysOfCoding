// write a program to find the sum to two numbers
#include <iostream>
using namespace std;

int main()
{
    int first = 0, second = 0;
    cout << "Enter the first number: ";
    cin >> first;
    cout << "Enter the second number: ";
    cin >> second;

    cout << first << " + " << second << " = " << first + second;
    cout << endl;
}